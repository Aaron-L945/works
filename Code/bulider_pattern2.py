class Waiter():
    def construct_pizza(self, bulider):
        self.bulider = bulider
        [step() for step in (bulider.prepare_dough, bulider.add_sauce, bulider.add_topping, bulider.bake)]

    @property
    def pizza(self):
        return self.bulider.pizza


def validate_style(buliders):
    pizza_style = input("please input m or n?")
    bulider = buliders.get(pizza_style, None)
    if buliders:
        return True, bulider()
    else:
        return False, None


def main():
    # 输入披萨的名称
    buliders = dict(m=MargaritaBuilder, c=CreamyBaconBuilder)
    valid_input = False
    bulider = None
    while not valid_input:
        valid_input, bulider = validate_style(buliders)  # 判断是否是有效的名称
    waiter = Waiter()
    waiter.construct_pizza(bulider)
    pizza = waiter.pizza
    print("Enjoy your %s" % (pizza))


if __name__ == '__main__':
    main()
